#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * return: final answer
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * return: subtract final answer
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * return: divide
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - add two integers
 * @a: first int
 * @b: second int
 * return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
